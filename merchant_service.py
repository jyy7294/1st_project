def get_merchant_category(
    merchant_name: str
) -> str:
    """
    가맹점명을 기준으로 표준 업종을 반환합니다.

    처리 순서:
    1. 유명 가맹점 사전 확인
    2. 업종 키워드 확인
    3. 둘 다 없으면 기타 반환
    """

    normalized_name = (
        merchant_name
        .strip()
        .lower()
        .replace(" ", "")
    )

    # 1. 유명 가맹점명으로 정확히 확인
    for merchant, category in MERCHANT_CATEGORY_MAP.items():
        normalized_merchant = (
            merchant
            .strip()
            .lower()
            .replace(" ", "")
        )

        if normalized_merchant in normalized_name:
            return category

    # 2. 가맹점명에 업종 키워드가 있는지 확인
    for category, keywords in CATEGORY_KEYWORDS.items():
        for keyword in keywords:
            normalized_keyword = (
                keyword
                .strip()
                .lower()
                .replace(" ", "")
            )

            if normalized_keyword in normalized_name:
                return category

    # 3. 판단할 수 없는 가맹점
    return "기타"


